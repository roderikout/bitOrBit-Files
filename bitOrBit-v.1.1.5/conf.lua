function love.conf(t)
  t.title = "Bit Or Bit"		-- The title of the window the game is in (string)
  t.author = "Rodrigo Garcia Casado"			-- The author of the game (string)
  t.identity = "Bit Or Bit test 0.1.5"						-- The name of the save directory (string)
  t.version = "0.10.2"						-- The LÖVE version this game was made for (string)
  t.console = false						-- Attach a console (boolean, Windows only)
  t.window.width = 1200					-- The window width (number)
  t.window.height = 800					-- The window height .(number)
end